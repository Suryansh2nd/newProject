package com.suryansh.BasicofJAVA;

public class areaOFcircle {
    public static void main(String[] args) {

        double r = 5;
        double pi = 3.1415;

        double area_0f_circle = pi*r*r;

        System.out.println("area of circle is "+area_0f_circle);


        // Circumference of Circle

        //double r = 5;
        //double pi = 3.1415;
        double h = 8;

        System.out.println("CMF of circle "+ pi*r*r*h);

        // Volume of sphere

        System.out.println("Volume of sphere "+ 4/3+pi*r*r*r);

    }
}
